package vo;

public class GogekVO {
	private int gobun, godarn; //고객번호, 담당자번호
	private String goname, goaddr, gojumin;	//고객이름, 주소, 주민번호
	public int getGobun() {
		return gobun;
	}
	public void setGobun(int gobun) {
		this.gobun = gobun;
	}
	public int getGodarn() {
		return godarn;
	}
	public void setGodarn(int godarn) {
		this.godarn = godarn;
	}
	public String getGoname() {
		return goname;
	}
	public void setGoname(String goname) {
		this.goname = goname;
	}
	public String getGoaddr() {
		return goaddr;
	}
	public void setGoaddr(String goaddr) {
		this.goaddr = goaddr;
	}
	public String getGojumin() {
		return gojumin;
	}
	public void setGojumin(String gojumin) {
		this.gojumin = gojumin;
	}

}
